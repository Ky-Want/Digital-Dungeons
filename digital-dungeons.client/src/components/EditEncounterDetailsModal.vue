<template>
  <div class="component">
    <div
      class="modal fade"
      id="encounterModal"
      tabindex="-1"
      aria-labelledby="encounterModalLabel"
      aria-hidden="true"
    >
      <div class="modal-dialog modal-lg">
        <div class="modal-content bg-secondary">
          <div class="modal-header">
            <h5 class="modal-title" id="encounterModalLabel">Edit Encounter</h5>
            <button
              type="button"
              class="btn-close"
              data-bs-dismiss="modal"
              aria-label="Close"
            ></button>
          </div>
          <div class="modal-body">
            <form
              action="submit"
              class="card text-secondary"
              @submit.prevent="editEncounter()"
            >
              <div class="card-body text-start">
                <div class="form-floating mb-3">
                  <input
                    type="text"
                    class="form-control"
                    v-model="editable.name"
                    required
                    name="name"
                    placeholder="Name:"
                    maxlength="50"
                  />
                  <label for="name">Name:</label>
                </div>
                <div class="form-floating mb-3">
                  <input
                    type="url"
                    class="form-control"
                    v-model="editable.coverImg"
                    name="coverImg"
                    placeholder="Cover Image:"
                    maxlength="10000"
                  />
                  <label for="coverImg">Cover Image:</label>
                </div>
                <div class="form-floating">
                  <textarea
                    type="text"
                    class="form-control textarea-height"
                    v-model="editable.desc"
                    name="description"
                    style="resize: none"
                    placeholder="Description:"
                    maxlength="500"
                  ></textarea>
                  <label for="description">Description:</label>
                </div>
                <div class="text-end">
                  <span>{{ editable.desc ? editable.desc.length : 0 }}</span>
                  <span>/ 500</span>
                </div>
              </div>
              <div class="modal-footer">
                <button
                  type="button"
                  class="btn btn-secondary"
                  data-bs-dismiss="modal"
                >
                  Close
                </button>
                <button
                  type="submit"
                  class="btn btn-primary"
                  data-bs-dismiss="modal"
                >
                  Edit this Encounter!
                </button>
              </div>
            </form>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import { ref, watchEffect } from "vue";
import { AppState } from "../AppState.js";
import { encountersService } from "../services/EncountersService.js";
import Pop from "../utils/Pop.js";

export default {
  props: {
    encounter: { type: Object, required: true },
  },
  setup() {
    let editable = ref({});

    watchEffect(() => {
      editable.value = { ...AppState.encounter };
    });

    return {
      editable,
      async editEncounter() {
        try {
          await encountersService.editEncounter(editable.value, encounter.id);
        } catch (error) {
          Pop.error(error);
        }
      },
    };
  },
};
</script>

